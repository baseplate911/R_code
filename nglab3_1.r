#ARITHEMATIC OPERATIONS
num1<-10
num2<-5
sum<-num1+num2
sub<-num1-num2
mul<-num1*num2
div<-num1/num2
exp<-num1^num2
modulo<-num1%%num2
print(paste("addition",sum))
print(paste("sustraction",sub))
print(paste("multiplication",mul))
print(paste("division",div))
print(paste("exponent",exp))
print(paste("modulus",modulo))

#COMPARSION OPERATIONS
a<-50
b<-40
is_eql<-a==b
is_nteql<-a!=b
is_grt<-a>b
is_sml<-a<b
is_grteq<-a>=b
is_smleq<-a<=b
print(paste("IS EQUAL",is_eql))
print(paste("IS NOT EQUAL",is_nteql))
print(paste("IS GREATER",is_grt))
print(paste("IS SMALLER",is_sml))
print(paste("IS GREATER OR EQUAL TO",is_grteq))
print(paste("IS SMALLER TO EQUAL TO",is_smleq))